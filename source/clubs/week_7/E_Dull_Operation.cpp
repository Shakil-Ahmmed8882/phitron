#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define nl cout << "\n"
#define FAST_IO ios_base::sync_with_stdio(false); cin.tie(NULL);


void solve() {
    ll N;
    if (!(cin >> N)) return; 
    
    cout << N << " " << N - 1 << "\n";
}

int main()
{
    FAST_IO;
    
    int T;
    if (!(cin >> T)) return 0;
    
    while (T--) {
        solve();
    }
    
    return 0;
}